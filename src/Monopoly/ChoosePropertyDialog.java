    /**
     * Another indexOf creatoin
     * @param n The name to be found
     * @return Index at which it resides
     */
    public int findPlayerIndex(String n)
    {
        for(int i = 0; i < p.getProperties().size(); i++)
        {
            if(n.compareTo(p.getProperties().get(i).getName()) == 0)
            {
                return i;
            }
        }
        
        return -1;
    }
