    private class StatsListener implements ActionListener
    {

        @Override
        public void actionPerformed(ActionEvent e) 
        {
            logPrintoutArea.setText("");
            Property pr = p.getProperties().
                    get(
                    findPlayerIndex(
                    (String)propertyList.
                    getSelectedItem()));
            
            if(pr instanceof RailRoad)
            {
                RailRoad r = (RailRoad)pr;
                logPrintoutArea.setText(r.toString());
            }
            else if(pr instanceof Utility)
            {
                Utility u = (Utility)pr;
                logPrintoutArea.setText(u.toString());
            }
            else
            {
                logPrintoutArea.setText(pr.toString());
            }
        }
        
    }
    
